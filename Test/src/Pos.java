public class Pos {
	private int row;
	private int colum;

	public int getRow() {
		return row;
	}

	public void setRow(int row) {
		this.row = row;
	}

	public int getColum() {
		return colum;
	}

	public void setColum(int colum) {
		this.colum = colum;
	}

	public Pos(int row, int colum) {
		super();
		this.row = row;
		this.colum = colum;
	}

}
